package Java.Training.OOPS.feb3;

public class Unique_elements
{
    public static void main(String[] args)
    {
        String s = "abcdefab";
        String u = "";
        for (int i = 0 , j = s.length()-1; i < s.length() && j >= 0; i ++ , j--)
        {
            char chf = s.charAt(i);
            char chl = s.charAt(j);
            if (chf == chl)
            {
                u = u + chf;
                System.out.println(u);
            }
        }
    }
}
